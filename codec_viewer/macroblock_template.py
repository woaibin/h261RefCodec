class MacroBlockTemplate:
    """
    Represents a fixed template for macroblocks.
    """
    def __init__(self, width: int, height: int):
        self.width = width
        self.height = height


class SubMacroBlockTemplate:
    """
    Represents a fixed template for sub-macroblocks.
    """
    def __init__(self, width: int, height: int):
        self.width = width
        self.height = height